export function adminT(lang: AdminLang, key: string, vars?: Record<string, string | number>): string {
  let str = T[lang]?.[key] ?? T.en[key] ?? key
  if (vars) {
    Object.entries(vars).forEach(([k, v]) => { str = str.replace(`{{${k}}}`, String(v)) })
  }
  return str
}
